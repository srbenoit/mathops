    /**
     * WABBITEMU SOURCE: utilities/label.c, "*get_bcalls" function.
     *
     * @param theModel the model
     * @return the array of BCalls
     */
    public static BCall[] getBcalls(final EnumCalcModel theModel) {

        switch (theModel) {
            case TI_83P:
            case TI_83PSE:
            case TI_84P:
            case TI_84PSE:
                return BCALLS_83P;
            case TI_84PCSE:
                return BCALLS_84PCSE;

            case INVALID_MODEL:
            case TI_73:
            case TI_81:
            case TI_82:
            case TI_83:
            case TI_85:
            case TI_86:
            default:
                break;
        }
        return null;
    }

    /**
     * WABBITEMU SOURCE: utilities/label.c, "*FindBcall" function.
     *
     * @param address the address
     * @param model   the model
     * @return name of the BCall if found, null if not
     */
    public static String findBcall(final int address, final EnumCalcModel model) {

        final BCall[] bcalls = getBcalls(model);
        if (bcalls == null) {
            return null;
        }

        for (int i = 0; bcalls[i].address != -1; ++i) {
            if (bcalls[i].address == address) {
                return bcalls[i].name;
            }
        }

        return null;
    }
