package dev.mathops.assessment.expression.editmodel;

/**
 * Possible shapes for a fraction construction.
 */
public enum EFractionShape {

    /** Vertical (horizontal line between numerator and denominator). */
    VERTICAL,

    /** Slanted (raised numerator, then a slash, then lowered denominator). */
    SLANT,

    /** Inline (numerator, then a slash, then the denominator). */
    HORIZONTAL
}
