package dev.mathops.assessment.document;

/**
 * Possible primary baselines used for layout.
 */
public enum EPrimaryBaseline {

    /** The typographic baseline. */
    TYPOGRAPHIC,

    /** Hanging baseline - typically top of ascent. */
    HANGING,

    /** The centerline (half the ascent). */
    CENTERLINE,

    /** The line on which plus or minus symbols are centered. */
    MATH_AXIS,
}
