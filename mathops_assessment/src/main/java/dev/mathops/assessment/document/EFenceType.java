package dev.mathops.assessment.document;

/**
 * Types of fenced construction.
 */
public enum EFenceType {

    /** A constant to indicate parentheses. */
    PARENTHESES,

    /** A constant to indicate square brackets. */
    BRACKETS,

    /** A constant to indicate vertical bars. */
    BARS ,

    /** A constant to indicate braces (curly brackets). */
    BRACES,

    /** A constant to indicate left brace only (curly bracket). */
    LBRACE,
}
